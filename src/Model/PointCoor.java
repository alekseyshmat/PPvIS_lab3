package Model;

public class PointCoor {
    private int x;
    private double y;

    public PointCoor() {
        x = 0;
        y = 0;
    }

    public int getX() {
        return x;
    }

    public double getY() {
        return y;
    }

    public void setX(int x) {
        this.x = x;
    }

    public void setY(double y) {
        this.y = y;
    }
}
